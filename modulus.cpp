#include <iostream>
using namespace std;

int main()
{

  int x = 5;
  int y = 2;
  int modulus = x % y;
  cout << "The modulus of " << x << " and " << y << " is: " << modulus << endl;
  return 0;
}

// modulus is the remainder of the division of one number by another. In this case, it calculates the remainder when 5 is divided by 2, which is 1.